#!/bin/python3

#math
print(50 + 50) #add
print(50 - 50) #subtract
print(50 * 50) #multiply
print(50 / 50) #divide

print(50 + 50 - 50 * 50 / 50) #PEMDAS

print(50 ** 2) #exponents, 50 to the power of 2
print(50 % 6) #modulo, remainder of division

print(50 / 6) #regular division
print(50 // 6) #divide with no leftovers
